class IgnorePOModule(FilteringModule):
    def __init__(self):
        self.ignored_po_pairs = IGNORED_PO_PAIRS

    def validate(self, cluster: Dict[str, Any]) -> bool:
        relation = cluster["predicted_relation"]
        arg2 = cluster["predicted_object"]

        return (relation, arg2) not in self.ignored_po_pairs

    def get_name(self):
        return f"IgnorePOModule - {len(self.ignored_po_pairs)} PO pairs"
